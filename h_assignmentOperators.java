public class h_assignmentOperators {
  public static void main(String[] args){
    int a=10;
    a+=5;  //this will add 5 in a
    System.out.println(a); // output will be 15
    a-=10;  //it will subtract 10 from a which is 15 till above 
    System.out.println(a); // output will be 5 now
    a*=7; //it will multiply a by 7
    System.out.println(a); //now value of a will be 35
    a/=5;  //it will devide a from 5
    System.out.println(a); //output will be 7 because we devide 35 which was the value of a by 5
  }
  
}
