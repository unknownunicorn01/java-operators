public class m_relationalOperators{
  public static void main(String[] args){
    int a=10;
    int b=20;
    System.out.println(a==b);  //it will print false because a and b are not equal
    System.out.println(a!=b);  //it will print true because a and b are not equal
    System.out.println(a!=10);  //it will print false because 10 is equal to 10
    System.out.println(a==10);  //it will print ture because 10 is equal to 10

    System.out.println(a<b); //it will print true because 10 is smaller then 20
    System.out.println(10<=10); //it will print true because 10 is not smaller the 10 but equal to 10
  }
  
}
